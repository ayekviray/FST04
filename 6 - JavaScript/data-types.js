// Primitive Data Types

// Strings: '' or "" = Textual data
let firstName = "John";
console.log("First Name:", firstName, "Special Character: !,?()");
console.log("The type of data is ", typeof firstName);
let age = 30;
console.log("My age is ", age);
console.log("The type of data is ", typeof age);

//Boolean: True or Fale
let student = true;
console.log("Are you a Student?", student);
console.log("The Typeof is ", typeof student);

//Null: Intentional absence of a value
let car = null;
console.log("Car:", car);
console.log("Typeof Car:", typeof car);
// Undefined: Unintentional absence of value
let city;
city = undefined;
console.log(city);
console.log(typeof city);
